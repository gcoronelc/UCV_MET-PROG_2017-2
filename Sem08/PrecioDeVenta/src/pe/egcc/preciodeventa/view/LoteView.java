  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    // datos
    String nombre = txtProducto.getText();
    double tamLote = Double.parseDouble(txtTamLote.getText());
    double preLote = Double.parseDouble(txtPreLote.getText());
    double porcGastosAdm = Double.parseDouble(txtPorcGasAdm.getText());
    double porcGanancia = Double.parseDouble(txtPorcGana.getText());
    LoteModel bean = new LoteModel(nombre, tamLote, preLote, porcGastosAdm, porcGanancia);
    // Proceso
    LoteController control = new LoteController();
    bean = control.procesarLote(bean);
    // Reporte
    txtUniComer.setText("" + bean.getUniComer());
    txtCosLote.setText("" + bean.getCosLote());
    txtCosUni.setText("" + bean.getCosUnidad());
    txtPreVenta.setText("" + bean.getPreVenta());
    activarControles(false);
  }//GEN-LAST:event_btnProcesarActionPerformed

